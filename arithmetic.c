#include<stdio.h>
int main ()
{
    int a=23, b=3;
    
    // using operators and printing results

    printf("a + b = %d\n",a+b);
    printf("a - b = %d\n",a-b);
    printf("a * b = %d\n",a*b);
    printf("a / b = %f\n",(float)a/b);
    printf("a%b = %d\n",a%b);
    printf("a++ = %d\n",a++);
    printf("a-- = %d\n",a--);

return 0;

}